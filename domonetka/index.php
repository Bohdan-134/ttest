  <script>
    // Таймер обратного отсчета
    function updateCountdownDisplay(hours, minutes, seconds) {
      document.querySelector('.countdown__hour .countdown__block').textContent = hours.toString().padStart(2, '0');
      document.querySelector('.countdown__minute .countdown__block').textContent = minutes.toString().padStart(2, '0');
      document.querySelector('.countdown__second .countdown__block').textContent = seconds.toString().padStart(2, '0');
    }

    // Function to start or resume the countdown timer
    function startTimer(duration) {
      const startTime = new Date().getTime();
      const endTime = startTime + duration;

      sessionStorage.setItem('endTime', endTime);

      const timerInterval = setInterval(() => {
        const now = new Date().getTime();
        const timeLeft = endTime - now;

        if (timeLeft <= 0) {
          clearInterval(timerInterval);
          sessionStorage.removeItem('endTime');
          updateCountdownDisplay(0, 0, 0); // Update the display when the timer ends
          console.log('Timer has ended');
        } else {
          const hours = Math.floor((timeLeft % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
          const minutes = Math.floor((timeLeft % (1000 * 60 * 60)) / (1000 * 60));
          const seconds = Math.floor((timeLeft % (1000 * 60)) / 1000);

          updateCountdownDisplay(hours, minutes, seconds);
        }
      }, 1000);
    }

    // Check if there is a countdown timer in progress
    const endTime = sessionStorage.getItem('endTime');
    if (endTime) {
      // Calculate remaining time and start the timer
      const timeLeft = endTime - new Date().getTime();
      if (timeLeft > 0) {
        const hours = Math.floor((timeLeft % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((timeLeft % (1000 * 60 * 60)) / (1000 * 60));
        const seconds = Math.floor((timeLeft % (1000 * 60)) / 1000);

        updateCountdownDisplay(hours, minutes, seconds);
        startTimer(timeLeft);
      } else {
        sessionStorage.removeItem('endTime');
        updateCountdownDisplay(0, 0, 0); // Update the display when the timer ends
        console.log('Timer has ended');
      }
    } else {
      // Start a new 40-minute countdown timer
      const duration = 40 * 60 * 1000; // 40 minutes in milliseconds
      startTimer(duration);
    }

